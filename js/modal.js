/* ---------- Helpers ---------- */
let currentGalleryIndex = 0;
let currentImages = [];

function openModal(projectId) {
  const data = projectsData[projectId];
  if (!data) return;

  currentImages = data.images;
  currentGalleryIndex = 0;

  const overlay = document.getElementById("projectModal");

  // Galerie
  const galleryEl = overlay.querySelector(".modal-gallery");
  galleryEl.innerHTML = "";

  currentImages.forEach((src, i) => {
    const img = document.createElement("img");
    img.src = src;
    img.alt = data.title;
    if (i === 0) img.classList.add("active");
    galleryEl.appendChild(img);
  });

  // Flèches + dots si plusieurs images
  if (currentImages.length > 1) {
    const prev = document.createElement("button");
    prev.className = "gallery-arrow prev";
    prev.innerHTML = "←";
    prev.onclick = () => changeSlide(-1);

    const next = document.createElement("button");
    next.className = "gallery-arrow next";
    next.innerHTML = "→";
    next.onclick = () => changeSlide(1);

    const dots = document.createElement("div");
    dots.className = "gallery-nav";
    currentImages.forEach((_, i) => {
      const dot = document.createElement("button");
      dot.className = "gallery-dot" + (i === 0 ? " active" : "");
      dot.onclick = () => goToSlide(i);
      dots.appendChild(dot);
    });

    galleryEl.appendChild(prev);
    galleryEl.appendChild(next);
    galleryEl.appendChild(dots);
  }

  // Texte
  overlay.querySelector(".modal-tag").textContent = data.tag;
  overlay.querySelector(".modal-year").textContent = data.year;
  overlay.querySelector(".modal-title").textContent = data.title;
  overlay.querySelector(".modal-desc").textContent = data.desc;

  // Outils
  const toolsWrap = overlay.querySelector(".modal-tools");
  toolsWrap.innerHTML = "";
  data.tools.forEach(tool => {
    const span = document.createElement("span");
    span.className = "modal-tool-tag";
    span.textContent = tool;
    toolsWrap.appendChild(span);
  });

  // Lien externe
  const linkEl = overlay.querySelector(".modal-link");
  if (data.link) {
    linkEl.href = data.link;
    linkEl.target = "_blank";
    linkEl.style.display = "inline-flex";
  } else {
    linkEl.style.display = "none";
  }

  overlay.classList.add("open");
  document.body.style.overflow = "hidden";
}

function closeModal() {
  const overlay = document.getElementById("projectModal");
  overlay.classList.remove("open");
  document.body.style.overflow = "";
}

function changeSlide(dir) {
  goToSlide(currentGalleryIndex + dir);
}

function goToSlide(index) {
  const overlay = document.getElementById("projectModal");
  const imgs = overlay.querySelectorAll(".modal-gallery img");
  const dots = overlay.querySelectorAll(".gallery-dot");

  if (index < 0 || index >= currentImages.length) return;

  imgs[currentGalleryIndex].classList.remove("active");
  if (dots[currentGalleryIndex]) dots[currentGalleryIndex].classList.remove("active");

  currentGalleryIndex = index;
  imgs[currentGalleryIndex].classList.add("active");
  if (dots[currentGalleryIndex]) dots[currentGalleryIndex].classList.add("active");
}

/* ---------- Événements ---------- */
document.addEventListener("DOMContentLoaded", () => {
  const overlay = document.getElementById("projectModal");

  // Fermer en cliquant en dehors
  overlay.addEventListener("click", (e) => {
    if (e.target === overlay) closeModal();
  });

  // Fermer avec Échap
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape") closeModal();
  });

  // Navigation clavier dans la galerie
  document.addEventListener("keydown", (e) => {
    if (!overlay.classList.contains("open")) return;
    if (e.key === "ArrowLeft") changeSlide(-1);
    if (e.key === "ArrowRight") changeSlide(1);
  });
});
